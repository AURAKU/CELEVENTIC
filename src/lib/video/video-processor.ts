// ─── Configuration ──────────────────────────────────────────────────────────

export function getExternalConverterPath(): string {
  return (
    process.env.CELEVENTIC_VIDEO_CONVERTER_PATH?.trim() ||
    // Generic alias — some ops docs/scripts refer to this env var name; both point at the
    // same production binary, first non-empty wins.
    process.env.VIDEO_PROCESSOR_PATH?.trim() ||
    "/usr/local/bin/celeventic-process-video"
  );
}

function getFfmpegBin(): string {
  return process.env.FFMPEG_PATH?.trim() || "ffmpeg";
}

function getFfprobeBin(): string {
  return process.env.FFPROBE_PATH?.trim() || "ffprobe";
}

export const MAX_OUTPUT_WIDTH = 1920;
export const MAX_OUTPUT_HEIGHT = 1080;

function getTimeoutMs(): number {
  const raw = Number(process.env.VIDEO_PROCESSOR_TIMEOUT_MS);
  return Number.isFinite(raw) && raw > 0 ? raw : 15 * 60 * 1000;
}

function getConcurrency(): number {
  const raw = Number(process.env.VIDEO_PROCESSOR_CONCURRENCY);
  return Number.isFinite(raw) && raw >= 1 ? Math.floor(raw) : 1;
}

/** Best-effort existence check for the VPS converter binary — cheap, safe to call per-request. */
export function hasExternalConverter(): boolean {
  try {
    return existsSync(getExternalConverterPath());
  } catch {
    return false;
  }
}

/**
 * Opt-in gate for the Hostinger VPS converter binary. Default is **off** so production always
 * uses the adaptive TypeScript FFmpeg path (capability detection + HDR fallback) even when
 * `/usr/local/bin/celeventic-process-video` exists on disk. Set `VIDEO_USE_EXTERNAL_CONVERTER`
 * to `true` / `1` / `yes` only after verifying that binary's filter graph matches this host's
 * ffmpeg build (notably: never hard-require `zscale` without a fallback).
 */
export function isExternalConverterOptedIn(): boolean {
  const raw = process.env.VIDEO_USE_EXTERNAL_CONVERTER?.trim().toLowerCase();
  return raw === "true" || raw === "1" || raw === "yes";
}

/** True only when the operator explicitly opted in *and* the binary is present on disk. */
export function shouldAttemptExternalConverter(): boolean {
  return isExternalConverterOptedIn() && hasExternalConverter();
}

